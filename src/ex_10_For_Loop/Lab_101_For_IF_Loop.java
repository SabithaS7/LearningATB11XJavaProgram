package ex_10_For_Loop;

public class Lab_101_For_IF_Loop {
    public static void main(String[] args) {
        for (int aadhya = 0; aadhya < 18; aadhya++) { // 0 to 17, 18 times
            if (aadhya > 15) {
                System.out.println("Gift from Pappa, IPhone");
            } else {
                System.out.println("No Gift");
            }
        }
    }
}

