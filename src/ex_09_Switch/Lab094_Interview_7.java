package ex_09_Switch;

public class Lab094_Interview_7 {
    public static void main(String[] args) {
        char code = 'C';
        switch (code) {
            default:
                System.out.println("Hellooooooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}
//prints the value Helloooooooo
